#pragma once

//====================================================
// BgmManager
//====================================================
// 【概要】
// ゲーム全体のBGM再生を一元管理するマネージャクラス。
// ・同時に再生されるBGMは常に1つだけ
// ・シーン遷移時のBGM停止／切替を安全に保証
// ・各Sceneは直接DxLibの音声APIを呼ばず、
//   必ず本クラス経由で操作する
//
// 【設計意図】
// - BGMの多重再生を物理的に防ぐ
// - シーン側の責務を「演出・ロジック」に限定
// - フェード処理や拡張を将来追加しやすくする
//====================================================
class BgmManager
{
public:
	//-------------------------
	// シングルトン取得
	//-------------------------
	// アプリケーション全体で唯一の
	// BgmManager インスタンスを返す
	static BgmManager& Instance();

	//-------------------------
	// BGM再生
	//-------------------------
	// soundHandle :
	//   DxLibでロード済みのサウンドハンドル
	//
	// ・同一BGMが再生中の場合は何もしない
	// ・別BGMが再生中の場合は必ず停止してから再生
	// ・再生形式はループ固定
	void Play(int soundHandle);

	//-------------------------
	// BGM停止
	//-------------------------
	// 現在再生中のBGMがあれば停止し、
	// 管理状態を初期化する
	void Stop();

	//-------------------------
	// 再生状態取得
	//-------------------------
	// return :
	//   true  : BGM再生中
	//   false : 停止中
	bool IsPlaying() const;

private:
	//-------------------------
	// コンストラクタ
	//-------------------------
	// 外部からの生成を禁止し、
	// Instance() 経由のみで利用させる
	BgmManager() = default;

	//-------------------------
	// 内部状態
	//-------------------------
	// 現在再生中のBGMハンドル
	// -1 : 再生なし
	int currentBgm = -1;
};
