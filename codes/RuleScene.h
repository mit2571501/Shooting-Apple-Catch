#pragma once
#include "Scene.h"
#include "common.h"

//====================================================
// RuleScene
// ルール説明専用シーン
//
// ・ゲーム操作方法や注意点を表示する
// ・Enterキー / コントローラーボタンで
//   タイトルシーンへ戻る
// ・ゲーム進行ロジックは一切持たない
//
// 設計方針:
//   - 表示専用の軽量シーン
//   - 入力は「戻る操作」のみ許可
//   - SceneManager に遷移責務を委譲
//====================================================
class RuleScene : public Scene
{
public:
	//--------------------------------------------
	// 初期化
	// シーン生成直後に一度だけ呼ばれる
	//--------------------------------------------
	void Init() override;

	//--------------------------------------------
	// 更新処理
	// Enter / Bボタンの入力監視のみ行う
	//--------------------------------------------
	void Update() override;

	//--------------------------------------------
	// 描画処理
	// 背景・半透明パネル・説明文を描画
	//--------------------------------------------
	void Draw() override;

private:
	//--------------------------------------------
	// 入力状態管理
	//--------------------------------------------
	int oldEnter = 0;   // Enterキーの前フレーム状態（押しっぱなし防止）

	//--------------------------------------------
	// 文字座標
	//--------------------------------------------
	int String_X = 120;	// ルール文x座標
	int String_Y = 150;	// ルール文y座標
	int String_I = 30;	// ルール文y座標間隔

	//--------------------------------------------
	// 背景制御
	//--------------------------------------------
	int bgX = 0;        // 背景スクロール用X座標

	int ApplePosX = gWindow.width / 6;	// リンゴのX座標
	int ApplePosY = gWindow.height - gWindow.height / 4;	// リンゴのY座標

	float AppleMaxSize = 1.0;	// リンゴの最大サイズ
	float AppleMinSize = 1.0;	// リンゴの最小サイズ
	float AppleSize = AppleMinSize;	// リンゴの現在のサイズ
	float AppleSizeChangeSpeed = 0.05 / 1000;	// リンゴのサイズ変化速度
	int AppleSizeDirection = 1;	// リンゴのサイズ変化方向（1:拡大, -1:縮小）

	float AppleRotation = 0;	// リンゴの回転角度
	float AppleRotationSpeed = 1.0 / 1000;	// リンゴの回転速度
};
