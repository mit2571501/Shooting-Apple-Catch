//======================================================
// 構造体定義
//======================================================


//----------------------------------
// 弾データ
//----------------------------------
struct Shot	// プレイヤーの弾
{
	int x, y;		// 座標
	bool isAlive;	// 発射中か
	bool isPose;	// ポーズ中か
};


//----------------------------------
// リンゴ（敵）データ
//----------------------------------
struct Apple	// 敵リンゴ
{
	int x, y;		// 座標

	bool isAlive;   // 出現中か
	bool fading;    // 消滅フェード中か
	bool isPose;    // ポーズ中か
	bool isCross;   // 当たり判定用フラグ（プレイヤーと交差しているか）

	int alpha;      // 透明度（255～0）

	int dx;         // 移動速度X
	int dy;         // ランダム移動用Y速度

	int type;       // AppleType
	int centerX;    // 円運動中心X
	int centerY;    // 円運動中心Y
};


//----------------------------------
// リンゴ出現確率テーブル
//----------------------------------
struct AppleWeight
{
	int type;       // AppleType
	int weight;     // 出現重み
};

extern AppleWeight appleTable[];	// 出現確率テーブル
extern const int APPLE_TABLE_SIZE;	// テーブル要素数


//----------------------------------
// 金リンゴ（ボス）
//----------------------------------
struct GoldenApple	// 金リンゴ
{
	bool  isAlive = false;	// 出現中か
	bool  isKill = false;	// 撃破済みか

	float x, y;				// 位置

	int radius;				// 当たり判定半径
	int hp;					// 耐久値
	int scoreValue;			// 撃破スコア
};


//----------------------------------
// 爆発エフェクト
//----------------------------------
struct Explosion	// 爆発エフェクト
{
	int x, y;
	int frame;     // 再生フレーム
	bool isAlive;  // 再生中か
};


//----------------------------------
// デバフゾーン
//----------------------------------
struct DangerZone	// 取得スコア半減ゾーン
{
	Rect rect;      // ゾーンの矩形

	bool isActive; // 有効か
	int  timer;    // 残り表示時間
};


//======================================================
// ウィンドウサイズ
//======================================================
struct WindowSize	// ウィンドウサイズ
{
	int width;	// 幅
	int height;	// 高さ
};

extern WindowSize gWindow;	// ウィンドウサイズ（初期化時に設定）


