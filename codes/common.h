//======================================================
// リンゴ種類定義
//======================================================
//
// type として Apple 構造体から参照される
//
enum AppleType
{
	APPLE_ZIGZAG = 0,	// 左右ジグザグ移動
	APPLE_SINE,			// サイン波移動
	APPLE_NORMAL,		// 直進
	APPLE_CIRCLE,		// 円運動
	APPLE_FAST,			// 加速ジグザグ
	APPLE_POISON,		// 毒リンゴ（減点）
	APPLE_MISS,			// ミスリンゴ（画面外に出ると減点）
	APPLE_TYPE_MAX
};

