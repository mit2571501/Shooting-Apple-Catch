//----------------------------------
// 点(x, y) が矩形内にあるか判定
// マウスクリックやタップの当たり判定などで使用
// プレイヤーが危険エリア内にいるかの判定にも使用
//
// @param x, y    : 判定する座標
// @param bx, by  : 矩形左上座標
// @param bw, bh  : 矩形サイズ
// @return        : 内側なら true
//----------------------------------
bool IsInside(int x, int y, int bx, int by, int bw, int bh)
{
	return (
		x >= bx && x <= bx + bw &&
		y >= by && y <= by + bh
		);
}
